package com.example.rfid.vo;

public class SpotVO {
    int SpotId;
    String SpotName;
    int SpotX;
    int SpotY;

    public int getSpotId() {
        return SpotId;
    }

    public void setSpotId(int spotId) {
        SpotId = spotId;
    }

    public String getSpotName() {
        return SpotName;
    }

    public void setSpotName(String spotName) {
        SpotName = spotName;
    }

    public int getSpotX() {
        return SpotX;
    }

    public void setSpotX(int spotX) {
        SpotX = spotX;
    }

    public int getSpotY() {
        return SpotY;
    }

    public void setSpotY(int spotY) {
        SpotY = spotY;
    }
}
